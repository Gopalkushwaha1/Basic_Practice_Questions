import java.util.*;
class Sum{
	public static void main(String[] args){
		Scanner sc = new Scanner(System.in);
		System.out.print("Enter your first number :: ");
		int a = sc.nextInt();
		System.out.print("Enter your Second number :: ");
		int b = sc.nextInt();
		System.out.println("Sum of " + a + " + " + b + " is :: " + (a+b));
	}
}
